    public boolean QINSERT(String item1, String item2, int item3) {
        if ((FRONT == 0 && REAR == N) || (FRONT == REAR + 1)) {
            System.out.println("OVERFLOW");
            return false;
        } else if (FRONT == -1) {
            FRONT = 0;
            REAR = 0;
        } else if (REAR == N) {
            REAR = 0;
        } else {
            REAR++;
        }
      Queue1.add(item3);
      Queue2.add(item1);
      Queue3.add(item2);
        return true;
    }
   

