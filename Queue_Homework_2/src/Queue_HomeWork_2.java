    public boolean QDELETE() {
        if (FRONT == -1) {
            System.out.println("UNDERFLOW");
            return false;
        }
        ITEM1 = Queue2.poll();
        ITEM2 = Queue3.poll();
        ITEM3 = Queue1.poll();
        if (FRONT == REAR) {
            FRONT = -1;
            REAR = -1;
        } else {
            if (FRONT == N) {
                FRONT = 0;
            } else {
                FRONT++;
            }
        }
        return true;
    }

